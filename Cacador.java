class Cacador {
  private int x;
  private int y;
  private boolean ammo;

  Cacador() {
    x = 0;
    y = 0;
    ammo = true;
  }

  public void setX(int x) {
    this.x = x;
  }
  public void setY(int y) {
    this.y = y;
  }
  public int getX() {
    return x;
  }
  public int getY() {
    return y;
  }



}
