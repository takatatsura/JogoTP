class Presa {
  private int x;
  private int y;
  private int ponto;

  Presa() {
    x = 500;
    y = 500;
    ponto = 0;
  }

  public void setX(int x) {
    this.x = x;
  }
  public void setY(int y) {
    this.y = y;
  }
  public int getX() {
    return x;
  }
  public int getY() {
    return y;
  }

}
